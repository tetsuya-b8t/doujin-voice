/**
 * FANZAから同人音声作品データを取得してsrc/data/works.tsを生成するスクリプト。
 * 実行: npm run fetch-works
 */

import { writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));

const API_ID = process.env.DMM_API_ID;
const AFFILIATE_ID = process.env.DMM_AFFILIATE_ID || 'doujinvoice-990';

if (!API_ID) {
  console.error('DMM_API_ID not set');
  process.exit(1);
}

// 音声作品を判定するジャンルキーワード
const AUDIO_GENRES = ['ASMR', 'バイノーラル', 'KU100', 'ささやき', '耳舐め', '耳かき', '催眠', '音声作品'];

// カテゴリ判定マッピング（優先順位順）
const CATEGORY_RULES = [
  { cat: 'hypno', keywords: ['催眠', '洗脳', 'ヒプノシス'] },
  { cat: 'ntr',   keywords: ['NTR', '寝取られ', '不倫', '寝取り'] },
  { cat: 'ts',    keywords: ['TS', '女体化', '性転換', '男の娘'] },
  { cat: 'yuri',  keywords: ['百合', 'GL', 'レズビアン', '女性同士'] },
  { cat: 'ninpu', keywords: ['妊婦', '妊娠中', '産後'] },
  { cat: 'asmr',  keywords: ['ASMR', 'バイノーラル', '耳かき', '添い寝', '睡眠', 'ささやき'] },
];

function detectCategory(genres, title = '') {
  const names = [...genres.map(g => g.name), title];
  for (const rule of CATEGORY_RULES) {
    if (rule.keywords.some(kw => names.some(n => n.includes(kw)))) {
      return rule.cat;
    }
  }
  return 'asmr';
}

function isAudioWork(item) {
  // volumeに「分」が含まれる = 音声作品（例: "約91分", "6本 (約94分)+α"）
  if (item.volume && item.volume.includes('分')) return true;
  // ジャンルに音声固有のものが含まれる
  const genres = item.iteminfo?.genre?.map(g => g.name) ?? [];
  return AUDIO_GENRES.some(kw => genres.some(g => g.includes(kw)));
}

async function fetchByKeyword(keyword, hits = 50) {
  const params = new URLSearchParams({
    api_id: API_ID,
    affiliate_id: AFFILIATE_ID,
    site: 'FANZA',
    service: 'doujin',
    floor: 'digital_doujin',
    hits: String(hits),
    keyword,
    sort: 'review',
    output: 'json',
  });
  const res = await fetch(`https://api.dmm.com/affiliate/v3/ItemList?${params}`);
  const data = await res.json();
  return data.result?.items ?? [];
}

function mapItem(item, id) {
  const genres = item.iteminfo?.genre ?? [];
  const category = detectCategory(genres, item.title ?? '');
  const tags = genres
    .map(g => g.name)
    .filter(n => !['男性向け', '成人向け', '専売'].includes(n))
    .slice(0, 6);

  const circle = item.maker?.name ?? item.iteminfo?.maker?.[0]?.name ?? '';
  const cvList = item.iteminfo?.voice ?? item.iteminfo?.actor ?? [];
  const cv = cvList.map(v => v.name).join('、') || '不明';

  const price = Number(item.prices?.price ?? 0);
  const rating = item.review?.average ? parseFloat(item.review.average) : 0;

  // affiliateUrlには生のFANZA商品URLを保存（buildAffiliateUrl()で後からラップ）
  const affiliateUrl = item.URL ?? '';
  const thumbnailUrl = item.imageURL?.large ?? item.imageURL?.list ?? '';

  const description = (item.comment ?? item.title ?? '').replace(/<[^>]*>/g, '').slice(0, 120);

  return { id: String(id), title: item.title ?? '', circle, cv, category, price, rating, tags, description, affiliateUrl, thumbnailUrl };
}

async function main() {
  console.log('Fetching FANZA doujin audio works...\n');

  // カテゴリごとにキーワード検索して音声作品を収集
  const queries = [
    'ASMR',
    'バイノーラル 耳かき',
    '添い寝 ささやき',
    '催眠 音声',
    '催眠 耳元',
    '深層催眠 音声',
    'NTR 音声',
    '百合 音声',
    'TS 音声',
    '妊婦 音声',
    '囁き 音声',
  ];

  const fetches = queries.map(q => fetchByKeyword(q, 50));
  const results = await Promise.all(fetches);

  // 全結果をマージして重複除去
  const seen = new Set();
  const allItems = results.flat().filter(item => {
    if (seen.has(item.content_id)) return false;
    seen.add(item.content_id);
    return isAudioWork(item);
  });

  console.log(`Raw audio items collected: ${allItems.length}`);

  // ratingでソートして上位60件を取得
  const sorted = allItems
    .filter(item => item.imageURL?.large || item.imageURL?.list)
    .sort((a, b) => parseFloat(b.review?.average ?? '0') - parseFloat(a.review?.average ?? '0'))
    .slice(0, 60);

  const works = sorted.map((item, i) => mapItem(item, i + 1));

  // カテゴリ分布を表示
  const dist = {};
  works.forEach(w => { dist[w.category] = (dist[w.category] ?? 0) + 1; });
  console.log('Category distribution:', dist);
  console.log('\nTop 5 works:');
  works.slice(0, 5).forEach(w => console.log(`  [${w.category}] ${w.title.slice(0, 45)} (${w.rating})`));

  // works.ts を生成
  const output = `// AUTO-GENERATED by scripts/fetch-fanza-works.mjs — do not edit manually
// Last updated: ${new Date().toISOString().slice(0, 10)}

export type Category = 'asmr' | 'ntr' | 'ts' | 'yuri' | 'ninpu' | 'hypno';

export const CATEGORY_LABELS: Record<Category, string> = {
  asmr:  'ASMR',
  ntr:   'NTR',
  ts:    'TS',
  yuri:  '百合',
  ninpu: '妊婦',
  hypno: '催眠',
};

export type Work = {
  id: string;
  title: string;
  circle: string;
  cv: string;
  category: Category;
  price: number;
  rating: number;
  tags: string[];
  description: string;
  affiliateUrl: string;
  thumbnailUrl: string;
};

export const works: Work[] = ${JSON.stringify(works, null, 2)};

export function getWorkById(id: string): Work | undefined {
  return works.find((w) => w.id === id);
}

export function getWorksByCategory(category: Category): Work[] {
  return works.filter((w) => w.category === category);
}

export function getAllTags(): string[] {
  const tags = new Set<string>();
  works.forEach((w) => w.tags.forEach((t) => tags.add(t)));
  return Array.from(tags).sort();
}

export function getWorksByTag(tag: string): Work[] {
  return works.filter((w) => w.tags.includes(tag));
}

export function getTopWorks(limit = 10): Work[] {
  return [...works].sort((a, b) => b.rating - a.rating).slice(0, limit);
}

export function getPickupWork(): Work | undefined {
  return works.find((w) => w.rating >= 4.5) ?? works[0];
}
`;

  const outPath = resolve(__dirname, '../src/data/works.ts');
  writeFileSync(outPath, output, 'utf-8');
  console.log(`\nWrote ${works.length} works to ${outPath}`);
}

main().catch(err => { console.error(err); process.exit(1); });
